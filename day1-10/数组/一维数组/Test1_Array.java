class Test1_Array {
public static void main(String[] args) {
	int x = 10;
	x = 20;

	System.out.println("x = " + x);

	//数据类型[ ] 数组名 = new 数据类型[ 数组的长度 ];
	int [ ] arr = new int [ 5 ];		//可以存储5个int类型的数

	/*
	左边:
	int:数据类型
	[ ]:代表的数组,几个[ ]就代表几维数组
	arr:数组名,是合法的标识符即可
	*/

	/*
	右边:
	new:创建的实体或对象
	int:数据类型
	[ ]:代表的数组
	5:代表数组的长度
	*/
	}
}