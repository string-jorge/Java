package com.qiao.otherio;

import java.io.FileInputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.PrintStream;

public class Test06_SystemInOut {

	/**
	 * @param args
	 * System.in是InputStream,标准输入流,可以默认从键盘输入读取字节数据
	 * System.out是PrintStream,是标准输出流,可以默认向Console(控制台)中输入字符和字节数据
	 * @throws IOException 
	 */
	public static void main(String[] args) throws IOException {
		//demo1();
		System.setIn(new FileInputStream("h.txt"));		//改变输入流
		System.setOut(new PrintStream("i.txt"));		//改变输出流
		
		InputStream is = System.in;				//获取标准的键盘输入流,默认指向键盘,改变后就指向文件
		PrintStream ps = System.out;			//获取标准输出流,默认指向控制台,改变后就指向文件
		
		int b;
		while((b = is.read()) != -1) {
			ps.write(b);
		}
		//System.out.println();					//也是一个输出流,不用关,因为没有和硬盘上的文件产生关联的管道
		is.close();
		ps.close();
	}

	private static void demo1() throws IOException {
		InputStream is1 = System.in;
		int x = is1.read();
		System.out.println(x);
		
		is1.close();
		
		InputStream is2 = System.in;
		int y = is2.read();
		System.out.println(is2);
	}

}
